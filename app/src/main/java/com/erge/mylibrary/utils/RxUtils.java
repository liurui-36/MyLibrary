public class RxUtils {

    public static <T> void run(ObservableOnSubscribe<T> ThreadRunnable, Observer<T> UIRunnable) {
        Observable
                .create(ThreadRunnable)
                .subscribeOn(Schedulers.io()) // 指定 subscribe() 发生在 IO 线程
                .observeOn(AndroidSchedulers.mainThread()) // 指定 Subscriber 的回调发生在主线程
                .subscribe(UIRunnable);
    }
}
